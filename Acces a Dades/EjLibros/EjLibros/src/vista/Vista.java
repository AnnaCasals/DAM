    private void actualiza(Libro lb) {
        jTextFieldTitulo.setText(lb.getTitulo());
        jTextFieldAutor.setText(lb.getAutor());
        jTextFieldGenero.setText(lb.getGenero());
        jTextFieldAnyo.setText(String.valueOf(lb.getAnyo()));
    }
    
    private Libro llenaLibro(){
        
        try {
            int anyo = Integer.parseInt(jTextFieldAnyo.getText());
            Libro lb = new Libro(jTextFieldTitulo.getText(),jTextFieldAutor.getText(),
            jTextFieldGenero.getText(), anyo);
            return lb;
        
        } catch (NumberFormatException ex){
            JOptionPane.showMessageDialog(null, "Datos incorrectos");
            lb = null;
            return lb;
        }
        
    }
    
