    private void jButtonAnyadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAnyadirActionPerformed
        if (jButtonAnyadir.getText().equals("+")){
            //vaciar textfield
            jTextFieldTitulo.setText("");
            jTextFieldAutor.setText("");
            jTextFieldGenero.setText("");
            jTextFieldAnyo.setText("");
        
            //deshabilitar resto botones
            jButtonPrimero.setEnabled(false);
            jButtonAnterior.setEnabled(false);
            jButtonSiguiente.setEnabled(false);
            jButtonUltimo.setEnabled(false);
            jButtonQuitar.setEnabled(false);
            jButtonActualizar.setEnabled(false);
         
            //cambiar texto botón
            jButtonAnyadir.setText("OK");
        
        } else {
           
            jButtonPrimero.setEnabled(true);
            jButtonAnterior.setEnabled(true);
            jButtonSiguiente.setEnabled(true);
            jButtonUltimo.setEnabled(true);
            jButtonQuitar.setEnabled(true);
            jButtonActualizar.setEnabled(true);
         
            //cambiar texto botón
            jButtonAnyadir.setText("+");
            lb = llenaLibro();
            
            //llamar al metodo nuevo del controlador y se mira si devuelve el libro correctamente
            
            if (lb != null) {
               
               c.nuevo(lb);
               
            } else {
                lb = c.anterior();
                actualiza(lb);                
                               
            }
        }
    }//GEN-LAST:event_jButtonAnyadirActionPerformed

    private void jButtonQuitarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonQuitarActionPerformed
        c.eliminar();
        lb = c.anterior();
        actualiza(lb);
    }//GEN-LAST:event_jButtonQuitarActionPerformed

    private void jButtonActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonActualizarActionPerformed
        Libro z = lb;
        if(jButtonActualizar.getText().equals("A")) {
            jTextFieldTitulo.setEditable(true);
            jTextFieldGenero.setEditable(true);
            jTextFieldAutor.setEditable(true);
            jTextFieldAnyo.setEditable(true);
            
            jButtonActualizar.setText("OK");
        } else {
            jTextFieldTitulo.setEditable(false);
            jTextFieldGenero.setEditable(false);
            jTextFieldAutor.setEditable(false);
            jTextFieldAnyo.setEditable(false);
            
            jButtonActualizar.setText("A");
            
            lb = llenaLibro();
            c.actualizar(z, lb);
        }
       
    }//GEN-LAST:event_jButtonActualizarActionPerformed

