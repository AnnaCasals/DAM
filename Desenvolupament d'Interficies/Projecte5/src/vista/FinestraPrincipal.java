    private void bAcceptarJ1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAcceptarJ1ActionPerformed
        // TODO add your handling code here:
        p = txtParaula.getText();
        p = p.toUpperCase();
        
        res = new char[p.length()];
        ll = new char[p.length()];
        String a = "";
        for(int i = 0; i < p.length(); i++) {
            if(p.charAt(i) == ' '){
                a += " ";
                res[i] = ' ';
            } else {
                a += "*";
                res[i] = '-';
            } 
        }
        
        txtParaula.setText(a);
        pasarParaula();
        paraula();
        
        txtParaula.setEnabled(false);
        bAcceptarJ1.setEnabled(false);
        
        txtLletra.setEnabled(true);
        bAcceptarJ2.setEnabled(true);
        
    }//GEN-LAST:event_bAcceptarJ1ActionPerformed

    private void bAcceptarJ2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAcceptarJ2ActionPerformed
        // TODO add your handling code here:
        String z = txtLletra.getText();
        if(comprovarNomesUnaLletra(z)) {
            z = z.toUpperCase();
            char a = z.charAt(0);
            if(!comprovaLletra(a)) {
                switch(cont) {
                    case 0:
                        lblImg.setIcon(new ImageIcon("src\\src\\foto2.png"));
                        break;
                    case 1:
                        lblImg.setIcon(new ImageIcon("src\\src\\foto3.png"));
                        break;
                    case 2:
                        lblImg.setIcon(new ImageIcon("src\\src\\foto4.png"));
                        break;
                    case 3:
                        lblImg.setIcon(new ImageIcon("src\\src\\foto5.png"));
                        JOptionPane.showMessageDialog(null, "Oh, has perdut");
                        tornaAComencar();
                        break;              
                }
                cont++;
                lblIntents.setText("" + cont);
            } else {
                paraula();
                if(!comprovaGuanyat()) {
                    JOptionPane.showMessageDialog(this, "Molt bé, ho has adivinat!!!");
                    tornaAComencar();
                } 
            }
            txtLletra.setText("");
        } else {
            txtLletra.setText("");
        }
        
    }//GEN-LAST:event_bAcceptarJ2ActionPerformed

