    public void pasarParaula() {
        for(int i = 0; i < p.length(); i++) {
            ll[i] = p.charAt(i);
        }
    }
    
    public void paraula() {
        String r = "";
        for(int i = 0; i < res.length; i++) {
            r += res[i] + " "; 
        }
        lblParaula.setText(r);
    }
    
    public boolean comprovaGuanyat() {
        boolean a = false;
        for(int i = 0; i < res.length; i++) {
            if(res[i] == '-') {
                a = true;
            }
        }
        return a;
    }
    
    public boolean comprovaLletra(char a) {
        boolean z = false;
        for(int i = 0; i < ll.length; i++) {
            if(ll[i] == a) {
                res[i] = ll[i];
                z = true;
            }
        }
        if(cont != 4) {
            if(!lletraDita(a)) {
                lletres += a + " ";
                lblLletres.setText(lletres);
            } else {
                JOptionPane.showMessageDialog(this, "La lletra ja està dita!!!");
                txtLletra.setText("");
            }   
        } 
        return z;
    }
    
    public boolean comprovarNomesUnaLletra(String z) {
        boolean a = true;
        if(z.length() != 1) {
            JOptionPane.showMessageDialog(this, "Només pots posar una lletra");
            a = false;
        }
        return a;
    }
    
    public void tornaAComencar() {
        lletres = " ";
        lblLletres.setText(" ");
        
        txtParaula.setEnabled(true);
        txtParaula.setText("");
        bAcceptarJ1.setEnabled(true);
        
        txtLletra.setEnabled(false);
        txtLletra.setText("");
        bAcceptarJ2.setEnabled(false);
                
        lblParaula.setText("");
        
        lblImg.setIcon(new ImageIcon("src\\src\\foto1.png"));
        
        cont = 0;
        lblIntents.setText(cont + "");
    }
    
    public boolean lletraDita(char a) {
        boolean z = false;
        for(int j = 0; j < lletres.length(); j++) {
            if(a == lletres.charAt(j)) {
                z = true;
            }
        }
        return z;
    }
    
