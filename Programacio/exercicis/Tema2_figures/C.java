package Tema2_figures;

public class C {
	public static void main(String[] args) {
		int a = 9;
		for(int i = 0; i < a / 3; i++) {
			for(int j = 0; j <= a; j++) {
				System.out.print("*");
			}
			System.out.println();
		}
		for(int l = 0; l <= a / 3; l++) {
			for(int k = 0; k <= a / 3; k++) {
				System.out.print("*");
			}
			System.out.println();
		}
		for(int i = 0; i < a / 3; i++) {
			for(int j = 0; j <= a; j++) {
				System.out.print("*");
			}
			System.out.println();
		}
		
	}
}
