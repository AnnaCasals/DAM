package Tema2_figures;

public class E {
	public static void main(String[] args ) {
		int a = 10;
		for(int b = 0; b < a / 5; b++) {
			for(int c = 0; c < a; c++) {
				System.out.print("*");
			}
			System.out.println();
		}
		for(int d = 0; d < a / 5; d++) {
			for(int e = 0; e <= a / 5; e++) {
				System.out.print("*");
			}
			System.out.println();
		}
		for(int f = 0; f < a / 5; f++) {
			for(int g = 0; g <= a / 2; g++) {
				System.out.print("*");
			}
			System.out.println();
		}
		for(int h = 0; h < a / 5; h++) {
			for(int i = 0; i <= a / 5; i++) {
				System.out.print("*");
			}
			System.out.println();
		}
		for(int j = 0; j < a / 5; j++) {
			for(int k = 0; k < a; k++) {
				System.out.print("*");
			}
			System.out.println();
		}
	}
}
