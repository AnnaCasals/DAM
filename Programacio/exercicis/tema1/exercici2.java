package tema1;

public class exercici2 {
	public static void main(String[] args) {
		int x = 2, y = 3;
		double n = 3.5, m = 1.5;
		System.out.println("x + n = " + (x + n) + " y - m = " + (y - m));
		System.out.println("n / x = " + (n / x) + " m * y = " + (m * y));
		System.out.println("y / m = " + (y / m));
	}
}
